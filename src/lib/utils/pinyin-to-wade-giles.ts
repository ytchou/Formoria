export function convertPinyinToWadeGiles(syllables: string[]): string[] {
  return syllables.map((syllable) => {
    const normalized = syllable.toLowerCase().replace(/[üv]/g, 'u')
    return PINYIN_TO_WG[normalized] ?? syllable
  })
}
